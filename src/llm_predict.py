"""
llm_predict.py
--------------
LLM-only directional prediction for AAPL on the test set,
using the same 70/15/15 split as train.py.

Conditions:
  A — price data only (last 5 days OHLCV + indicators)
  B — news only (daily summary from llm_features.csv)
  C — price + news

Models:
  gpt    — gpt-5-nano via OpenAI API (OPENAI_API_KEY in .env)
  ollama — llama3.2 via local Ollama at http://localhost:11434

Usage:
  python src/llm_predict.py --condition A --model gpt
  python src/llm_predict.py --condition B --model ollama --dry-run
  python src/llm_predict.py --condition C --model gpt --days 3
"""

import argparse
import json
import os
import re
from pathlib import Path

import pandas as pd
from dotenv import load_dotenv

load_dotenv()

PRICES_CSV  = Path("data/processed/prices.csv")
CACHE_DIR   = Path("data/processed")

# News summary CSVs — one per LLM, generated by llm_features.py --model <x>
LLM_FEAT_CSV = {
    "ollama": Path("data/processed/llm_features.csv"),
    "gpt":    Path("data/processed/llm_features_gpt.csv"),
}

TRAIN_FRAC = 0.70
VAL_FRAC   = 0.15

_JSON_RE  = re.compile(r"\{.*\}", re.DOTALL)
_FALLBACK = {"direction": "up", "confidence": 0.5, "reasoning": "Parse failed."}


# ── Prompt builder ────────────────────────────────────────────────────────────

def build_prompt(condition: str, trading_day, price_window: pd.DataFrame,
                 news_summary: str) -> str:
    sections = [
        "You are a financial analyst predicting next-day AAPL stock movement.\n"
    ]

    if condition in ("A", "C"):
        sections.append("Recent AAPL data (last 5 trading days):")
        sections.append("Date | Close | MA5 | MA20 | Daily Return | Volatility")
        for idx, row in price_window.iterrows():
            sections.append(
                f"{idx.date()} | ${row['close']:.2f} | ${row['ma5']:.2f} | "
                f"${row['ma20']:.2f} | {row['daily_return'] * 100:+.2f}% | "
                f"{row.get('volatility20', float('nan')):.4f}"
            )
        sections.append("")

    if condition in ("B", "C"):
        news_text = news_summary if news_summary else "(No news available for this day.)"
        sections.append(
            f"Today's news headlines (published before market close):\n{news_text}"
        )
        sections.append("")

    sections.append(
        "Based on this information, predict whether AAPL's closing price will be "
        "HIGHER or LOWER tomorrow compared to today.\n\n"
        'Respond with JSON only:\n'
        '{"direction": "up" or "down", "confidence": 0.0-1.0, "reasoning": "one sentence"}'
    )

    return "\n".join(sections)


# ── Response parsing ──────────────────────────────────────────────────────────

def _parse_response(content: str) -> dict | None:
    try:
        return json.loads(content)
    except (json.JSONDecodeError, TypeError):
        pass
    match = _JSON_RE.search(content or "")
    if match:
        try:
            return json.loads(match.group())
        except json.JSONDecodeError:
            pass
    return None


# ── LLM callers ───────────────────────────────────────────────────────────────

def call_gpt(prompt: str, retries: int = 3) -> dict:
    from openai import OpenAI
    client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))
    for attempt in range(retries):
        try:
            response = client.chat.completions.create(
                model="gpt-5-nano",
                messages=[
                    {
                        "role": "system",
                        "content": "You are a financial analyst. Always respond with valid JSON only.",
                    },
                    {"role": "user", "content": prompt},
                ],
                response_format={"type": "json_object"},
            )
            content = response.choices[0].message.content
            result = _parse_response(content)
            if result:
                return result
            print(f"\n    GPT attempt {attempt + 1}: could not parse response", end="")
        except Exception as e:
            print(f"\n    GPT attempt {attempt + 1} failed: {e}", end="")
    print("\n    All GPT attempts failed — using fallback.")
    return _FALLBACK.copy()


def call_ollama(prompt: str, retries: int = 3) -> dict:
    import ollama
    for attempt in range(retries):
        content = None
        try:
            response = ollama.chat(
                model="llama3.2",
                messages=[{"role": "user", "content": prompt}],
                format="json",
                options={"temperature": 0.1},
            )
            content = (
                response.message.content
                if hasattr(response, "message")
                else response["message"]["content"]
            )
            result = _parse_response(content)
            if result:
                return result
            print(f"\n    Ollama attempt {attempt + 1}: could not parse response", end="")
        except Exception as e:
            print(f"\n    Ollama attempt {attempt + 1} failed: {e}", end="")
    print("\n    All attempts failed — using fallback.")
    return _FALLBACK.copy()


# ── Accuracy reporting ────────────────────────────────────────────────────────

def compute_accuracy(cache_path: Path, prices: pd.DataFrame) -> None:
    predictions = pd.read_csv(cache_path, parse_dates=["date"])
    if predictions.empty:
        print("No predictions cached yet.")
        return
    predictions = predictions.set_index("date")
    merged = predictions.join(prices[["target"]], how="inner")
    if merged.empty:
        print("No predictions matched test-set dates.")
        return
    actual_dir = merged["target"].apply(lambda x: "up" if x > 0 else "down")
    correct = (merged["direction"] == actual_dir).sum()
    total = len(merged)
    acc = correct / total * 100
    print(f"\nDirectional Accuracy: {acc:.1f}%  ({correct}/{total} correct)")


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="LLM-only directional prediction for AAPL.")
    parser.add_argument("--condition", choices=["A", "B", "C"], required=True,
                        help="A=price only  B=news only  C=price+news")
    parser.add_argument("--model", choices=["gpt", "ollama"], required=True,
                        help="gpt=gpt-5-nano (OpenAI)  ollama=llama3.2 (local)")
    parser.add_argument("--dry-run", action="store_true",
                        help="Print prompts without calling any API.")
    parser.add_argument("--days", type=int, default=None,
                        help="Stop after processing N unprocessed days.")
    args = parser.parse_args()

    # Load price data and determine test split
    prices = pd.read_csv(PRICES_CSV, index_col="date", parse_dates=True)
    n = len(prices)
    train_end = int(n * TRAIN_FRAC)
    val_end   = train_end + int(n * VAL_FRAC)
    test_dates = prices.index[val_end:]
    print(
        f"Loaded {n} trading days. Test set: {len(test_dates)} days "
        f"({test_dates[0].date()} → {test_dates[-1].date()})"
    )

    # Load cached news summaries for the selected model (keyed by date)
    feat_csv = LLM_FEAT_CSV[args.model]
    llm_feat: dict = {}
    if feat_csv.exists():
        feat_df = pd.read_csv(feat_csv, index_col="date", parse_dates=True)
        for d, row in feat_df.iterrows():
            llm_feat[d.date()] = str(row.get("summary", ""))
        print(f"Loaded {len(llm_feat)} news summaries from {feat_csv}")
    elif args.condition in ("B", "C"):
        print(
            f"Warning: {feat_csv} not found.\n"
            f"  Run: python src/llm_features.py --model {args.model}\n"
            f"  Conditions B and C will have no news text until then."
        )

    # Set up cache file
    CACHE_DIR.mkdir(parents=True, exist_ok=True)
    cache_path = CACHE_DIR / f"llm_predict_{args.condition}_{args.model}.csv"

    if cache_path.exists():
        done_dates = set(pd.read_csv(cache_path, usecols=["date"])["date"].astype(str))
        print(f"Resuming — {len(done_dates)} days already cached.")
    else:
        done_dates = set()
        pd.DataFrame(columns=["date", "direction", "confidence", "reasoning"]).to_csv(
            cache_path, index=False
        )

    # Process each test day
    processed = 0
    for day in test_dates:
        day_str = str(day.date())
        if day_str in done_dates:
            continue
        if args.days is not None and processed >= args.days:
            print(f"Reached --days limit ({args.days}). Stopping.")
            break

        pos = prices.index.get_loc(day)
        if pos < 5:
            continue
        price_window = prices.iloc[pos - 5 : pos]
        news_summary = llm_feat.get(day.date(), "")

        prompt = build_prompt(args.condition, day.date(), price_window, news_summary)

        if args.dry_run:
            print(f"\n{'─' * 64}\nDay: {day_str}")
            print(prompt)
            print(f"{'─' * 64}")
            result = {"direction": "up", "confidence": 0.5, "reasoning": "[DRY RUN]"}
        else:
            print(f"[{day_str}] ", end="", flush=True)
            result = call_gpt(prompt) if args.model == "gpt" else call_ollama(prompt)
            print(f"{result.get('direction', '?')} ({result.get('confidence', 0.5):.2f})")

        direction  = str(result.get("direction", "up")).lower().strip()
        direction  = direction if direction in ("up", "down") else "up"
        confidence = max(0.0, min(1.0, float(result.get("confidence", 0.5))))
        reasoning  = str(result.get("reasoning", "")).replace("\n", " ").strip()

        pd.DataFrame([{
            "date":       day_str,
            "direction":  direction,
            "confidence": confidence,
            "reasoning":  reasoning,
        }]).to_csv(cache_path, mode="a", header=False, index=False)
        processed += 1

    print(f"\nDone. Results saved → {cache_path}")
    compute_accuracy(cache_path, prices)


if __name__ == "__main__":
    main()
